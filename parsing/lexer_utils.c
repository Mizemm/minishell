#include "../minishell.h"

int special_char(char c)
{
	if (c == ' ' || c == '\t' || c == '\'' || c == '\"' || c == '|'
		| c == '<' || c == '>')
		return (1);
	return (0);
}
int find_dollar(char *str)
{
	int i;
	
	i = 0;
	while (str[i])
	{
		if (str[i] == '$')
			return (1);
		i++;
	}
	return (0);
}
